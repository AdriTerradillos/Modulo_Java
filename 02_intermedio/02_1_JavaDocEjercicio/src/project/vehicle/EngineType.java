package project.vehicle;

/**
 * Enumeración que define los distintos tipos de motor disponibles para los vehículos.
 *
 * @author Gamma Tech School
 * @version 1.0
 */
public enum EngineType {
    /**
     * Motor de gasolina.
     */
    PETROL,

    /**
     * Motor diésel.
     */
    DIESEL,

    /**
     * Motor completamente eléctrico.
     */
    ELECTRIC,

    /**
     * Motor híbrido (combinación de combustión y eléctrico).
     */
    HYBRID
}